/**
 * Shared mock for lucide-react
 * Exports every icon used in the codebase as a simple span component.
 * Must use explicit named exports for ESM compatibility (--experimental-vm-modules).
 */
import React from 'react';

type IconProps = Record<string, unknown>;

const createIcon = (name: string) => {
  const Icon = (props: IconProps) =>
    React.createElement('span', { 'data-testid': `icon-${name}`, ...props }, name);
  Icon.displayName = name;
  return Icon;
};

